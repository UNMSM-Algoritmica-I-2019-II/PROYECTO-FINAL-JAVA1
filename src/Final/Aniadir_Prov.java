    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        menu menu = new menu();
        String cod = codigo.getText();
        String empresap = empresa.getText();
        String celularp = celular.getText();
        String telefonop = telefono.getText();
        String direccionp = direccion.getText();
        Connection cn;
        Statement s;
        ResultSet rs;

        try{
            cn= basedatos.conectar();
            s = cn.createStatement();
            rs= s.executeQuery("insert into proveedores (codigo,empresa,telefono,celular,direccion)"
                + " values ("+cod+",'"+empresap+"',"+celularp+","+telefonop+",'"+direccionp+"')");

            rs.close();
            cn.close();

        }catch(Exception e){
            System.out.println(e);
        }

        
        this.dispose();//cerrar esta ventana
        menu.setVisible(true);//hacer visible el objeto menu
        menu.setLocationRelativeTo(null);//hacer que la ventana menu aparezca al centro
    }//GEN-LAST:event_aceptarActionPerformed

